destination_places = ["Sweden" , "Croatia", "Norway", "Canada", "Ireland"]
#list just as is:
print(destination_places)
print("\n")

#modified list for printing:
print(sorted(destination_places))
print("\n")

#show original list again:
print(destination_places)
print("\n")

#reversed sorted list:
print(sorted(destination_places,reverse=True))
print("\n")

#original list ...again:
print(destination_places)
print("\n")

#permanently reverse list:
destination_places.reverse()
print(destination_places)
print("\n")

#reverse back:
destination_places.reverse()
print(destination_places)
print("\n")

#permanently sort list :
destination_places.sort()
print(destination_places)
print("\n")

#permanently reverse sort list:
destination_places.sort(reverse=True)
print(destination_places)

