person_name = " \n\tada \n\tlovelace "
# the \n puts the word on a new line and the \t tabs the word in

print(person_name) #to print with all the whitespace
print(person_name.lstrip()) #to strip left-side whitespace
print(person_name.rstrip()) # to strip right-side whitespace
print(person_name.strip()) #to strip all whitespace